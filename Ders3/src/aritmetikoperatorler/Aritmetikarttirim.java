package aritmetikoperatorler;

public class Aritmetikarttirim {
    public static void main(String[] args){
        int a=2, b=7, c=40, d=50;
        a++; //değişkenii ata sonra 1 kere arttır
        System.out.println("değişkenii ata sonra 1 kere arttır "  +a++);
        ++b; // Değişkenimi birkere arttır sonra ata
        System.out.println("Değişkenimi birkere arttır sonra ata " + ++b);
        c--; // Değişkenimi Ata Sonra 1 Kere Eksilt
        System.out.println("Değişkenimi Ata Sonra 1 Kere Eksilt " +c--);
        --d; // Değişkenimi 1 Kere Azalt Sonra Ata
        System.out.println("Değişkenimi 1 Kere Azalt Sonra Ata " +--d);



    }
}
